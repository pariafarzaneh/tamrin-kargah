# دریافت نام و سال تولد از کاربر
name = input("لطفاً نام خود را وارد کنید: ")
birth_year = int(input("لطفاً سال تولد خود را وارد کنید (مثلاً 2000): "))

# محاسبه سن تقریبی
current_year = 2025
age = current_year - birth_year

# نمایش پیام خوش‌آمدگویی
print(f"{name} عزیز، خوش آمدی! سن تقریبی شما {age} سال است.")